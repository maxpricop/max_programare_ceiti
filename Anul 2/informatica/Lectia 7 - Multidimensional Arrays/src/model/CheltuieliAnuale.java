	public int calculateMonthlyCheltuieli(int month) {
		if (month < 1 || month > 12) return 0;

		int sum = 0;

		for (int i = 0; i < cheltuieli[month - 1].length; i++) {
			sum += cheltuieli[month - 1][i];
		}

		return sum;
	}

	public int calculateYearlyCheltuieli() {
		int sum = 0;

		for (int i = 0; i < 12; i++) {
			sum += calculateMonthlyCheltuieli(i + 1);
		}

		return sum;
	}
